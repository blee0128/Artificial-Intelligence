def depth_limited_search(problem, limit=50):
    """[Figure 3.17]"""

    explored = set()

    def recursive_dls(node, problem, limit):

        if problem.goal_test(node.state):
            # print the total generated nodes for the algorithm
            # to reach the goal state
            print("Total nodes generated: " + str(len(explored)))
            return node
        elif limit == 0:
            return 'cutoff'
        else:
            cutoff_occurred = False
            for child in node.expand(problem):
                #the below block of code will remove any cycle back to its ancestor which
                #was discussed in class to improve the efficiency of the IDS algorithm
                x = child.path()
                x.remove(child)

                if child in x:
                    continue

                explored.add(child.state)
                result = recursive_dls(child, problem, limit - 1)
                if result == 'cutoff':
                    cutoff_occurred = True
                elif result is not None:
                    return result
            return 'cutoff' if cutoff_occurred else None

    # Body of depth_limited_search:
    return recursive_dls(Node(problem.initial), problem, limit)


